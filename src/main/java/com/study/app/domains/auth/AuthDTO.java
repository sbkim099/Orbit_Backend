package com.study.app.domains.auth;

public class AuthDTO {
	private String id;
	private String pw;
	
	public AuthDTO() {}
	
	public AuthDTO(String id, String pw) {
		super();
		this.id = id;
		this.pw = pw;
	}
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPw() {
		return pw;
	}
	public void setPw(String pw) {
		this.pw = pw;
	}
}